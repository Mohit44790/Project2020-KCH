								<h5>Primary Nephrotic Syndrome Treatment Options</h5>
									<p>Treating primary Nephrotic Syndrome includes preserving kidney function as well as taking steps to reduce high blood pressure, swelling, high cholesterol, and the risks of infection. Treatment may vary from patient to patient, depending on age, disease, genetic results, kidney function and how well they respond to steroids.</p>
								<h5>Prednisone (steroids)</h5>
									<p>Usually, patients are treated with prednisone first. Prednisone belongs to a class of drugs known as corticosteroids. Corticosteroids work by decreasing the body's immune system response to various diseases or conditions that produce symptoms such as swelling, inflammation, and allergic-type reactions.</p>
									<p>To learn more about prednisone and Nephrotic Syndrome, please visit&nbsp;<a href="index.php" target="_blank" rel="noopener">the KCH website.</a></p>
								<h5>Second-line treatments</h5>
									<p>If steroids fail to cause remission (usually within 8-12 weeks), or if a patient becomes "steroid dependent," a second-line treatment may be tried. Some second-line treatments include:</p>
									<p>Adrenocorticotropin (ACTH Acthar Gel)<br> Angiotensin II receptor blockers (ARBs)<br> Angiotensin-converting enzyme (ACE) inhibitors<br> Cyclophosphamide (Cytoxan)<br> Cyclosporine (Neoral)<br> Methylprednisolone (Solu Medrol)<br> Mycophenolate (MMF, Cellcept, Myfortic)<br> Plasmapheresis (Liposorber)<br> Prograf (Tacrolimus)<br> Rituxan (Rituximab)</p>
								<h5>Clinical Trials</h5>
									<p>There are a number of other potential treatments that may reduce proteinuria (protein in the urine) in primary Nephrotic Syndrome patients. You can use this website to find a list of clinical trials for which you may be eligible. Ask your doctor about these trials and whether they might be right for you.</p>
								<h5>Additional Information</h5>
									<p>Your doctor may recommend a combination of treatments. Sometimes, a specific diet and other lifestyle changes will be recommended in lieu of or in addition to medications.</p>
