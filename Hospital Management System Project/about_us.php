            <h1>About KCH Hospital</h1>
            <p align="justify">Shri Kshetra Dharmasthala has immensely contributed to the spiritual, economic and social development of Karnataka.  It is imparting education to thousands of students all over Karnataka through its  KCH Educational Society wing. KCH College of Medical sciences &amp; Hospital in Dharwad, apart from providing quality medical education, is creating a revolution in health care scenario of Northern Karnataka.  It is a brain child of Dr. D. Veerendra Heggade, Rev. President of KCH Educational Society.  Way back in year 2001, a group of people from Dharwad approached Dr. Heggade and requested him to start a Hospital.  Dr. Heggade, understanding the health care needs of this part of Karnataka decided to start a Hospital with a Medical College attached to it.  On March 3rd, 2002, Mr. S. M. Krishna, the then Chief Minister laid the foundation stone for the Hospital building..</p>
            <p align="justify">KCH Hospital is located on the highway connecting Hubli and Dharwad.  It is spread over 65 acres of land, in a serene atmosphere. This campus, apart from Medical College and Hospital, houses Nursing Institute, Physiotherapy College, Students Hostels, Staff Quarters and abodes for patient's relatives.</p>
            <p align="justify">"Royal treatment for common man" has been the mission statement of KCH Hospital. Every person who has taken treatment in this Hospital would certainly feel this.  The clean environment and humble services of staff have been making plenty of people recover their health every day. Hospital is also fostering quality research in medical field.  Regular workshops and conferences are being conducted to enhance the knowledge of doctors working within the Hospital and also those within twin cities. For the community, Hospital conducts regular free health camps, family planning camps, cataract camps, spirometry camps etc.  Within very short span since its inception, under the able leadership of Dr. Niranjan Kumar, Medical Director, this Hospital has made a prominent name in the medical map of India. </p>
